public class CarteMoins10 extends Carte {

    public CarteMoins10()
    {
        super(-10);
    }
    /* règle : Si la carte «-10» est jouée, on soustrait 10
    du dernier total annoncé et on l’annonce à
    haute voix. On peut même éventuellement
    obtenir momentanément un total négatif
    inférieur à zéro. */
}
